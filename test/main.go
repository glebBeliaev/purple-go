// **Описание**: Создайте структуру для хранения пользователей в памяти и реализуйте метод фильтрации пользователей по возрасту с возвратом всех подходящих записей
//
// **Входные данные**: Структура User с полями ID (int), Name (string), Age (int) и минимальный возраст для фильтрации (int)
//
// **Выходные данные**: Срез структур User, содержащий всех пользователей с возрастом больше или равным указанному значению
//
// **Ограничения**:
// - Используйте slice для хранения пользователей в памяти
// - Метод должен возвращать новый срез с отфильтрованными результатами
// - Если подходящих пользователей нет, верните пустой срез
// - Исходный срез не должен изменяться
//
// **Примеры**:
// Входные данные: фильтрация по возрасту >= 25 в списке пользователей
// Output: []User{{ID: 2, Name: "Alice", Age: 30}, {ID: 3, Name: "Bob", Age: 25}}
//
// Входные данные: фильтрация по возрасту >= 50 когда таких пользователей нет
// Output: []User{}

type User struct {
	ID   int
	Name string
	Age  int
}

type UserRepo struct {
	users []User
}

func NewUserRepo() *UserRepo {
	return &UserRepo{
		users: []User{},
	}
}

func (r *UserRepo) FilterByAge(minAge int) []User {
	for i := range r.users {
		if r.users[i].Age < minAge {
			r.users = append(r.users[:i], r.users[i+1:]...)
			i--
		}
	}
	return r.users
}

